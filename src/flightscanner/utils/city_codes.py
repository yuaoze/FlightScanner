# 预先构建 IATA 代码 → 机场中文名的反向索引（供 UI 展示用）
_AIRPORT_CODE_TO_NAME: Dict[str, str] = {
    airport["code"]: airport["name"]
    for airports in DOMESTIC_AIRPORT_MAP.values()
    for airport in airports
}


def get_airport_name(code: str) -> str:
    """根据 IATA 代码返回机场中文名，未知代码直接返回代码本身。

    Args:
        code: IATA 机场代码（如 "PEK"）。

    Returns:
        机场中文名（如 "首都国际机场"），未知代码返回原代码。
    """
    return _AIRPORT_CODE_TO_NAME.get(code, code)


def get_airport_list(city: str) -> List[Dict[str, str]]:
    """返回城市的机场列表（含 IATA 代码和中文名），未知城市返回空列表。

    Args:
        city: 中文城市名称。

    Returns:
        机场信息列表，每项包含 ``code``（IATA 代码）和 ``name``（中文名）。
    """
    return DOMESTIC_AIRPORT_MAP.get(city, [])


def is_international_route(origin: str, destination: str) -> bool:
    """判断是否需要使用 Qunar 国际/跨国搜索页面。

    Qunar 页面区分：
    - oneway_list.htm：两端都是中国城市
    - oneway_list_inter.htm：至少一端不是中国城市
               (包含真正的国际航班、非中国→非中国、中国→非中国 等)

    注：虽然 MNL→CEB（菲律宾→菲律宾）不是"真正的国际航班"，
    但 Qunar 仍要求使用 oneway_list_inter.htm 处理任何涉及非中国城市的路由。

    Args:
        origin: 出发城市中文名。
        destination: 目的城市中文名。

    Returns:
        True 表示需要使用 _inter 页面，False 表示使用普通页面。
    """
    origin_domestic = origin in _DOMESTIC_CITIES
    dest_domestic = destination in _DOMESTIC_CITIES

    # 只有两端都在中国时，才使用普通页面
    # 任何包含非中国城市的路由，都需要用 _inter 页面
    return not (origin_domestic and dest_domestic)
